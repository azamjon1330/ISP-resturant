// Category meta: emoji + i18n key + Russian source name (matches admin CATS)
export const CATEGORY_META = [
  { src: 'Основные блюда', emoji: '🍛', key: 'catMain' },
  { src: 'Супы',           emoji: '🍲', key: 'catSoup' },
  { src: 'Гриль',          emoji: '🥩', key: 'catGrill' },
  { src: 'Выпечка',        emoji: '🥖', key: 'catBakery' },
  { src: 'Напитки',        emoji: '🥤', key: 'catDrinks' },
  { src: 'Десерты',        emoji: '🍰', key: 'catDessert' },
]

export function categoryMeta(catName) {
  const found = CATEGORY_META.find(c => c.src === catName)
  if (found) return found
  return { src: catName, emoji: '🍽️', key: 'catOther' }
}

export function getLang() {
  return localStorage.getItem('shop_lang') || 'uz'
}
export function setLang(code) {
  localStorage.setItem('shop_lang', code)
}

export function t(key, lang) {
  const L = lang || getLang()
  return (DICT[L] && DICT[L][key]) || DICT.uz[key] || key
}
